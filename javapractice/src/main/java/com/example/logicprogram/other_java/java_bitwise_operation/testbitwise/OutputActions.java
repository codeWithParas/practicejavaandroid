package com.example.logicprogram.other_java.java_bitwise_operation.testbitwise;

public class OutputActions {

    boolean Blink;
    boolean Beep;
    boolean Vibrate;
    boolean OutputNumeric;

    public boolean isBlink() {
        return Blink;
    }

    public void setBlink(boolean blink) {
        Blink = blink;
    }

    public boolean isBeep() {
        return Beep;
    }

    public void setBeep(boolean beep) {
        Beep = beep;
    }

    public boolean isVibrate() {
        return Vibrate;
    }

    public void setVibrate(boolean vibrate) {
        Vibrate = vibrate;
    }

    public boolean isOutputNumeric() {
        return OutputNumeric;
    }

    public void setOutputNumeric(boolean outputNumeric) {
        OutputNumeric = outputNumeric;
    }

    public boolean isOutputAlphanumeric() {
        return OutputAlphanumeric;
    }

    public void setOutputAlphanumeric(boolean outputAlphanumeric) {
        OutputAlphanumeric = outputAlphanumeric;
    }

    boolean OutputAlphanumeric;
}
